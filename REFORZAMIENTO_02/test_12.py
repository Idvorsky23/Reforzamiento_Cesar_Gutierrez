# Lista con diferentes tipos de datos
lista_mixta = ['Cesar', 'Gutierrez', 2, 7.6, 'Python', True, False, -89, 6.02]
# Lista con números pares
lista_numeros_pares = [2, 4, 6, 8, 10, 12]
#Suma de listas
suma_de_listas = lista_mixta + lista_numeros_pares
#Imprimir resultados
print("La lista mixta es:", lista_mixta)
print("La lista de numeros pares es:", lista_numeros_pares)
#El resultado final es la concetenación de ambas listas en una sola
print("La suma de las listas:", suma_de_listas)
