# Definir dos listas: una vacía y otra con elementos
lista_vacia = [ ]
lista_no_vacia = [1, 2, 3]

# Verificar si la lista vacía está vacía
if not lista_vacia:
    print("La lista está vacía.")
else:
    print("La lista no está vacía.")

# Verificar si la lista con elementos está vacía
if not lista_no_vacia:
    print("La lista está vacía.")
else:
    print("La lista no está vacía.")